
public class Address {
	/* Identification parameters */
	public String name;
	public Integer id;
	
	/* Connection parameters */
	public String hostname;
	public Integer port;
	
	
	/* Constructor */
	public Address() {
		this.name = null;
		this.id = 0;
		this.hostname = null;
		this.port = 0;
	}
}
